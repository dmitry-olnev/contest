#!/bin/bash
# Сложение двух чисел.

# Регулярное выражение для чисел с плавающей точкой
regex='^[-+]?[0-9]+([.,][0-9]+)?([eE][-+]?[0-9]+)?$'

# Проверяем, что передано ровно два аргумента
if [ "$#" -ne 2 ]; then
    echo "Ошибка: требуется два аргумента."
    exit 1
fi

# Проверяем первый аргумент
if ! [[ $1 =~ $regex ]]; then
    echo "Ошибка: первый аргумент '$1' не является числом."
    exit 1
fi

# Проверяем второй аргумент
if ! [[ $2 =~ $regex ]]; then
    echo "Ошибка: второй аргумент '$2' не является числом."
    exit 1
fi

# Заменяем запятую на точку для корректной обработки чисел в bash
arg1=$(echo "$1" | sed 's/,/./g')
arg2=$(echo "$2" | sed 's/,/./g')

# Складываем числа
result=$(awk "BEGIN {print $arg1 + $arg2}")

# Выводим результат
echo "Результат сложения: $result"


